export const formatCount = (count) => {
  if (count < 1000) {
    return count.toString()
  } else if (count >= 1000 && count < 1000000) {
    return (count / 1000).toFixed(count % 1000 === 0 ? 0 : 1) + 'k'
  } else if (count >= 1000000) {
    return (count / 1000000).toFixed(count % 1000000 === 0 ? 0 : 1) + 'm'
  }
}

export const loadModels = async () => {
  const modelPath = '/models'
  try {
    await faceapi.nets.ssdMobilenetv1.loadFromUri(modelPath)
    await faceapi.nets.faceLandmark68Net.loadFromUri(modelPath)
    await faceapi.nets.faceRecognitionNet.loadFromUri(modelPath)
  } catch (error) {
    console.error('Error loading models:', error)
  }
}

export async function compareFaces(file1, file2) {
  await loadModels()

  const img1 = await faceapi.bufferToImage(file1)
  const img2 = await faceapi.bufferToImage(file2)

  const [desc1, desc2] = await Promise.all([
    faceapi.detectSingleFace(img1).withFaceLandmarks().withFaceDescriptor(),
    faceapi.detectSingleFace(img2).withFaceLandmarks().withFaceDescriptor(),
  ])

  if (!desc1 || !desc2)
    throw new Error('Face not detected in one or both images.')

  const distance = faceapi.euclideanDistance(desc1.descriptor, desc2.descriptor)
  return distance < 0.6 // threshold (adjust for strictness)
}

